SELECT ENAME Employee, JOB Job, HIREDATE "Hire Date" FROM EMP
      WHERE HIREDATE BETWEEN '20-FEB-1981' AND '01-MAY-1981'
        ORDER BY HIREDATE ASC;

SELECT ENAME Employee, DEPTNO "Department No" FROM EMP
      WHERE DEPTNO = 10 OR DEPTNO = 30
        ORDER BY ENAME ASC;

SELECT ENAME Employee, SAL "Monthly Salary" FROM EMP
      WHERE (DEPTNO = 10 OR DEPTNO = 30) AND SAL > 1500;

SELECT ENAME Employee, HIREDATE "Hire Date" FROM EMP 
      WHERE HIREDATE BETWEEN '01-JAN-1982' AND '31-DEC-1982';

SELECT ENAME Employee, JOB Job FROM EMP 
      WHERE MGR IS NULL;

SELECT ENAME Employee, SAL Salary,COMM Commission FROM EMP
      WHERE COMM IS NOT NULL
        ORDER BY SAL DESC, COMM DESC;

SELECT ENAME Employee FROM EMP
